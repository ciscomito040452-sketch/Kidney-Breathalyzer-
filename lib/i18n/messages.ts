export function t(locale: AppLocale, key: MessageKey): string {
  return messages[locale][key];
}

export function formatGender(locale: AppLocale, gender: string): string {
  const map: Record<string, MessageKey> = {
    female: "genderFemale",
    male: "genderMale",
    other: "genderOther",
  };
  const key = map[gender];
  return key ? t(locale, key) : gender;
}

export function getGreeting(locale: AppLocale, hour = new Date().getHours()): string {
  if (hour < 12) return t(locale, "greetingMorning");
  if (hour < 17) return t(locale, "greetingAfternoon");
  if (hour < 21) return t(locale, "greetingEvening");
  return t(locale, "greetingNight");
}

export function getRiskShortLabels(locale: AppLocale) {
  return {
    low: t(locale, "riskLow"),
    moderate: t(locale, "riskModerate"),
    high: t(locale, "riskHigh"),
  } as const;
}

const PERIOD_KEYS: Record<HistoryPeriod, MessageKey> = {
  today: "periodToday",
  this_week: "periodThisWeek",
  this_month: "periodThisMonth",
  last_7: "periodDays7",
  last_30: "periodDays30",
  last_90: "periodDays90",
};

export function getHistoryPeriodLabel(
  locale: AppLocale,
  period: HistoryPeriod
): string {
  return t(locale, PERIOD_KEYS[period]);
}
